export type BaseMessageCatalog = MessageCatalog;
export type MenuMessageKey = keyof MessageCatalog['menu'];
export type InventoryMessageKey = keyof MessageCatalog['inventory'];
export type BlockMessageKey = keyof MessageCatalog['blocks'];
export type RecipeMessageKey = keyof MessageCatalog['recipes'];
export type RecipeTextPartKey = keyof MessageCatalog['recipes'][RecipeMessageKey];
export type SpecialKeyCode = keyof MessageCatalog['controls']['specialKeys'];

const specialKeyCodeSet = new Set<SpecialKeyCode>(
  Object.keys(fr.controls.specialKeys) as SpecialKeyCode[],
);

export const isSpecialKeyCode = (value: string): value is SpecialKeyCode =>
  specialKeyCodeSet.has(value as SpecialKeyCode);
